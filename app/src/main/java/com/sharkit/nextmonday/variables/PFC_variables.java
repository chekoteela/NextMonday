package com.sharkit.nextmonday.variables;

public class PFC_variables {
    public static String bar_code;
    public static int protein, fat, carbohydrate;
    public static float watter;


    public static int getProtein() {
        return protein;
    }

    public static void setProtein(int protein) {
        PFC_variables.protein = protein;
    }

    public static int getFat() {
        return fat;
    }

    public static void setFat(int fat) {
        PFC_variables.fat = fat;
    }

    public static int getCarbohydrate() {
        return carbohydrate;
    }

    public static void setCarbohydrate(int carbohydrate) {
        PFC_variables.carbohydrate = carbohydrate;
    }

    public static float getWatter() {
        return watter;
    }

    public static void setWatter(float watter) {
        PFC_variables.watter = watter;
    }

    public static String getBar_code() {
        return bar_code;
    }

    public static void setBar_code(String bar_code) {
        PFC_variables.bar_code = bar_code;
    }
}
