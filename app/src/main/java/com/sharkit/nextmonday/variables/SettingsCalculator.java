package com.sharkit.nextmonday.variables;

public class SettingsCalculator {

    private float weight, watter, desired_weight, activity, current_weight;
    private int protein, carbohydrate, fat, height, age, calorie;
    private String sex, target, formula;

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getWatter() {
        return watter;
    }

    public void setWatter(float watter) {
        this.watter = watter;
    }

    public float getDesired_weight() {
        return desired_weight;
    }

    public void setDesired_weight(float desired_weight) {
        this.desired_weight = desired_weight;
    }

    public int getProtein() {
        return protein;
    }

    public void setProtein(int protein) {
        this.protein = protein;
    }

    public int getCarbohydrate() {
        return carbohydrate;
    }

    public void setCarbohydrate(int carbohydrate) {
        this.carbohydrate = carbohydrate;
    }

    public int getFat() {
        return fat;
    }

    public void setFat(int fat) {
        this.fat = fat;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getActivity() {
        return activity;
    }

    public void setActivity(float activity) {
        this.activity = activity;
    }

    public String getTarget() {
        return target;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public String getFormula() {
        return formula;
    }

    public void setFormula(String formula) {
        this.formula = formula;
    }

    public float getCurrent_weight() {
        return current_weight;
    }

    public void setCurrent_weight(float current_weight) {
        this.current_weight = current_weight;
    }

    public int getCalorie() {
        return calorie;
    }

    public void setCalorie(int calorie) {
        this.calorie = calorie;
    }
}
